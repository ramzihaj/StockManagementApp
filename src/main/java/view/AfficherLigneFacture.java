    private void jButtonAjouterDetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAjouterDetActionPerformed
       String numArt = jTextFieldNumArt.getText().trim();
       String libelle = jTextFieldLibelleArt.getText().trim();
       String prixU = jTextFieldPrixUArt.getText().trim();
       String qte = jTextFieldQteArt.getText().trim();

try {
    // Validation des entrées
    if (numArt.isEmpty() || libelle.isEmpty() || prixU.isEmpty() || qte.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Tous les champs doivent être remplis.", "Erreur", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Conversion en nombres pour le calcul
    double prixUVal = Double.parseDouble(prixU);
    int qteVal = Integer.parseInt(qte);
    double prixTVal = prixUVal * qteVal; // Calcul du prix total

    // Afficher le prix total dans le champ texte (optionnel)
    jTextFieldPrixTArt.setText(String.valueOf(prixTVal));

    // Connexion à la base de données
    String url = "jdbc:mysql://localhost:3306/stock_manager";
    String login = "root";
    String pwd = "";

    // Étape 1 : Charger le pilote JDBC
    Class.forName("com.mysql.cj.jdbc.Driver"); // Nouvelle version du driver

    // Étape 2 : Établir la connexion
    Connection conn = DriverManager.getConnection(url, login, pwd);
    System.out.println("Connexion réussie");

    // Étape 3 : Préparer la requête d'insertion
    String req = "INSERT INTO lignefacture (numArt, libelle, prixU, Qte, prixT) VALUES (?, ?, ?, ?, ?)";
    PreparedStatement pst = conn.prepareStatement(req);
    pst.setString(1, numArt);
    pst.setString(2, libelle);
    pst.setDouble(3, prixUVal); // Passer la valeur double pour prixU
    pst.setInt(4, qteVal);      // Passer la valeur entière pour Qte
    pst.setDouble(5, prixTVal); // Passer la valeur double pour prixT

    // Exécution de la requête
    int rowsInserted = pst.executeUpdate();
    if (rowsInserted > 0) {
        JOptionPane.showMessageDialog(this, "Ligne Facture ajoutée avec succès !", "Succès", JOptionPane.INFORMATION_MESSAGE);

        // Afficher une autre interface après la création
        new AfficherLigneFacture().setVisible(true);
        this.setVisible(false);
    }

    // Fermer la connexion
    pst.close();
    conn.close();

} catch (NumberFormatException e) {
    JOptionPane.showMessageDialog(this, "Veuillez entrer des valeurs valides pour le prix unitaire et la quantité.", "Erreur de saisie", JOptionPane.ERROR_MESSAGE);
} catch (ClassNotFoundException ex) {
    Logger.getLogger(AfficherLigneFacture.class.getName()).log(Level.SEVERE, null, ex);
    JOptionPane.showMessageDialog(this, "Erreur de chargement du pilote JDBC.", "Erreur", JOptionPane.ERROR_MESSAGE);
} catch (SQLException ex) {
    Logger.getLogger(AfficherLigneFacture.class.getName()).log(Level.SEVERE, null, ex);
    JOptionPane.showMessageDialog(this, "Erreur d'accès à la base de données.", "Erreur", JOptionPane.ERROR_MESSAGE);
}

    }//GEN-LAST:event_jButtonAjouterDetActionPerformed

