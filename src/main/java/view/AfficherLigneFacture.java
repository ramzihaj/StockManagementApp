    private void jButtonModifierDetModActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModifierDetModActionPerformed
        String updatedNumArt = jTextFieldNumArtMod.getText();
        String updatedLibelle = jTextFieldLibelleArtMod.getText();
        String updatedPrixU = jTextFieldNumArtMod.getText();
        String updatedQte = jTextFieldLibelleArtMod.getText();
        
        
    try {
        String url = "jdbc:mysql://localhost:3306/stock_manager";
        String login = "root";
        String pwd = "";

        // Step 1: Load the driver
        Class.forName("com.mysql.jdbc.Driver");

        // Step 2: Establish the connection
        Connection conn = DriverManager.getConnection(url, login, pwd);
        

        // Step 3: Prepare the SQL Update query
        String updateQuery = "UPDATE lignefacture SET numart = ?, libelle = ?, prixU = ?, qte = ? WHERE numart = ?";

        // Step 4: Use PreparedStatement to update
        PreparedStatement pstmt = conn.prepareStatement(updateQuery);
        pstmt.setString(1, updatedNumArt);
        pstmt.setString(2, updatedLibelle);
        pstmt.setString(3, updatedPrixU);
        pstmt.setString(4, updatedQte);
        pstmt.setString(3, updatedNumArt);
       

        // Step 5: Execute the update
        int rowsAffected = pstmt.executeUpdate();
        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(this, "Information updated successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Update failed. Please try again.", "Error", JOptionPane.ERROR_MESSAGE);
        }

        // Step 6: Close resources
        pstmt.close();
        conn.close();
    } catch (ClassNotFoundException ex) {
        Logger.getLogger(AfficherLigneFacture.class.getName()).log(Level.SEVERE, null, ex);
    } catch (SQLException ex) {
        Logger.getLogger(AfficherLigneFacture.class.getName()).log(Level.SEVERE, null, ex);
    } 
    }//GEN-LAST:event_jButtonModifierDetModActionPerformed

