    private void jButtonSupprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSupprimerActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTableSupprimerFour.getModel();

        // Vérifier si une ligne est sélectionnée
        int selectedRow = jTableSupprimerFour.getSelectedRow();
        if (selectedRow != -1) {
            // Récupérer la valeur de la colonne "id" (ou autre clé primaire) de la ligne sélectionnée
            String id = model.getValueAt(selectedRow, 0).toString();

            // Demander confirmation
            int response = JOptionPane.showConfirmDialog(
                this,
                "Voulez-vous vraiment supprimer cette ligne ?",
                "Confirmation de suppression",
                JOptionPane.YES_NO_OPTION
            );

            // Si l'utilisateur confirme
            if (response == JOptionPane.YES_OPTION) {
                try {
                    // Connexion à la base de données
                    String url = "jdbc:mysql://localhost:3306/stock_manager";
                    String login = "root";
                    String pwd = "";
                    Connection conn = DriverManager.getConnection(url, login, pwd);

                    // Supprimer la ligne de la base de données
                    String query = "DELETE FROM client WHERE id = ?";
                    PreparedStatement pst = conn.prepareStatement(query);
                    pst.setString(1, id);
                    int rowsAffected = pst.executeUpdate();

                    // Vérifier si la suppression a réussi
                    if (rowsAffected > 0) {
                        // Supprimer la ligne du modèle de table
                        model.removeRow(selectedRow);
                        JOptionPane.showMessageDialog(this, "Fournisseur supprimé avec succès !");
                    } else {
                        JOptionPane.showMessageDialog(this, "Échec de la suppression dans la base de données.", "Erreur", JOptionPane.ERROR_MESSAGE);
                    }

                    // Fermer les ressources
                    pst.close();
                    conn.close();
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, "Erreur de connexion à la base de données : " + ex.getMessage(), "Erreur", JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Veuillez sélectionner une ligne à supprimer.", "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonSupprimerActionPerformed

