    private void jButtonValideAjouActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonValideAjouActionPerformed
        String nomFour = jTextFieldNomFour.getText().trim();
        String adresseFour = jTextFieldAdresseFour.getText().trim();
        String contactFour = jTextFieldContactFour.getText().trim();

        try {
            String url = "jdbc:mysql://localhost:3306/stock_manager";
            String login = "root";
            String pwd = "";

            // Étape 1 : Charger le pilote JDBC
            Class.forName("com.mysql.cj.jdbc.Driver");

            // Étape 2 : Établir la connexion
            Connection conn = DriverManager.getConnection(url, login, pwd);
            System.out.println("Connexion réussie");

            // Étape 3 : Préparer la requête d'insertion
            String req = "INSERT INTO fournisseur ( nomFour, adresseFour, contactFour ) VALUES (?, ?, ?)";
            PreparedStatement pst = conn.prepareStatement(req);
            pst.setString(1, nomFour);
            pst.setString(2, adresseFour);
            pst.setString(3, contactFour);
            int rowsInserted = pst.executeUpdate();
            if (rowsInserted > 0) {
                JOptionPane.showMessageDialog(this, "Fournisseur ajouter avec succès !", "Succès", JOptionPane.INFORMATION_MESSAGE);

                // Afficher une autre interface après la création
                new GererFournisseur().setVisible(true);
                this.setVisible(false);
            }

            // Fermer la connexion
            pst.close();
            conn.close();

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(GererFournisseur.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Erreur de chargement du pilote JDBC.", "Erreur", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException ex) {
            Logger.getLogger(GererFournisseur.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Erreur d'accès à la base de données.", "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonValideAjouActionPerformed

