    private void jButtonSupprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSupprimerActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTableSupprimerCli.getModel();

    // V√©rifier si une ligne est s√©lectionn√©e
    int selectedRow = jTableSupprimerCli.getSelectedRow();
    if (selectedRow != -1) {
        // R√©cup√©rer la valeur de la colonne "id" (ou autre cl√© primaire) de la ligne s√©lectionn√©e
        String id = model.getValueAt(selectedRow, 0).toString(); 

        // Demander confirmation
        int response = JOptionPane.showConfirmDialog(
            this,
            "Voulez-vous vraiment supprimer cette ligne ?",
            "Confirmation de suppression",
            JOptionPane.YES_NO_OPTION
        );

        // Si l'utilisateur confirme
        if (response == JOptionPane.YES_OPTION) {
            try {
                // Connexion √† la base de donn√©es
                String url = "jdbc:mysql://localhost:3306/stock_manager";
                String login = "root";
                String pwd = "";
                Connection conn = DriverManager.getConnection(url, login, pwd);

                // Supprimer la ligne de la base de donn√©es
                String query = "DELETE FROM client WHERE id = ?";
                PreparedStatement pst = conn.prepareStatement(query);
                pst.setString(1, id);
                int rowsAffected = pst.executeUpdate();

                // V√©rifier si la suppression a r√©ussi
                if (rowsAffected > 0) {
                    // Supprimer la ligne du mod√®le de table
                    model.removeRow(selectedRow);
                    JOptionPane.showMessageDialog(this, "Client supprim√© avec succ√®s !");
                } else {
                    JOptionPane.showMessageDialog(this, "√âchec de la suppression dans la base de donn√©es.", "Erreur", JOptionPane.ERROR_MESSAGE);
                }

                // Fermer les ressources
                pst.close();
                conn.close();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Erreur de connexion √† la base de donn√©es : " + ex.getMessage(), "Erreur", JOptionPane.ERROR_MESSAGE);
            }
        }
    } else {
        JOptionPane.showMessageDialog(this, "Veuillez s√©lectionner une ligne √† supprimer.", "Erreur", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButtonSupprimerActionPerformed

