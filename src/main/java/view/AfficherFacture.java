    private void jButtonRefreshActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRefreshActionPerformed
        try {
            String url = "jdbc:mysql://localhost:3306/stock_manager";
            String login = "root";
            String pwd = "";

            // √âtape 1 : Charger le pilote JDBC
            Class.forName("com.mysql.jdbc.Driver");

            // √âtape 2 : √âtablir la connexion
            Connection conn = DriverManager.getConnection(url, login, pwd);
            System.out.println("Connexion r√©ussie");

            // √âtape 3 : Ex√©cuter la requ√™te
            String query = "SELECT id, numArt, libelle, prixU, qte, prixT FROM lignefacture";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(query);

            // √âtape 4 : Ajouter les donn√©es au mod√®le de table
            while (rs.next()) {
                String numArt = rs.getString("numart");
                String libelle = rs.getString("libelle");
                String id = rs.getString("id");
                String prixU = rs.getString("prixU");
                String qte = rs.getString("qte");
                String prixT = rs.getString("prixT");
                
                DefaultTableModel model = (DefaultTableModel) jTableLigneFacture.getModel();
                model.addRow(new Object[]{id, numArt, libelle, prixU, qte, prixT });
            }

            // Fermer les ressources
            rs.close();
            st.close();
            conn.close();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AfficherFacture.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AfficherFacture.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonRefreshActionPerformed

